function darkTheme() {
    document.documentElement.style.setProperty('--text-primary', '#ffffff');
    document.documentElement.style.setProperty('--text-light', '#f2f2f2');
    document.documentElement.style.setProperty('--text-dark', '#f2f2f2');
    document.documentElement.style.setProperty('--primary-color', '#3f3f3f');
    document.documentElement.style.setProperty('--primary-color-dark', '#f2f2f2');
    document.documentElement.style.setProperty('--bg-light', '#1d1d1d');
    document.documentElement.style.setProperty('--bg-dark', '#272727');
    document.documentElement.style.setProperty('--bg-color', '#3f3f3f');
    document.documentElement.style.setProperty('--bg-default', '#292929');
    document.documentElement.style.setProperty('--color-same', '#00a39b');
    document.documentElement.style.setProperty('--input-color', '#3f3f3f');
    document.documentElement.style.setProperty('--icon-white-color', '#00a39b');
    document.documentElement.style.setProperty('--gray-dark', '#3F3F3F');
}

